import 'dart:math';

class _WeightedValue {
  const _WeightedValue(this.value, this.weight);

  final String value;
  final int weight;
}

class _NamePool {
  const _NamePool({
    required this.firstNames,
    required this.lastNames,
  });

  final List<_WeightedValue> firstNames;
  final List<_WeightedValue> lastNames;

  String nextFirstName(Random random) => _pickWeighted(firstNames, random);
  String nextLastName(Random random) => _pickWeighted(lastNames, random);

  static String _pickWeighted(List<_WeightedValue> values, Random random) {
    final totalWeight = values.fold<int>(
      0,
      (sum, entry) => sum + entry.weight,
    );
    var draw = random.nextInt(totalWeight);
    for (final entry in values) {
      if (draw < entry.weight) {
        return entry.value;
      }
      draw -= entry.weight;
    }
    return values.last.value;
  }
}

