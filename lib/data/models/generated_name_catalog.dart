  static String generateUniquePlayerName({
    required Random random,
    required Set<String> usedNamesLowercase,
    String? nationality,
    String fallbackPrefix = 'CPU',
  }) {
    final pool = _poolsByNationality[nationality] ?? _fallbackPool;

    for (var attempt = 0; attempt < 180; attempt += 1) {
      final firstName = pool.nextFirstName(random);
      final lastName = pool.nextLastName(random);
      final candidate = '$firstName $lastName';
      if (usedNamesLowercase.add(candidate.toLowerCase())) {
        return candidate;
      }
    }

    var suffix = usedNamesLowercase.length + 1;
    while (true) {
      final fallbackName = '$fallbackPrefix $suffix';
      if (usedNamesLowercase.add(fallbackName.toLowerCase())) {
        return fallbackName;
      }
      suffix += 1;
    }
  }
